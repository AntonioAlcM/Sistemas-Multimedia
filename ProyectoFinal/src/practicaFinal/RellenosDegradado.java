    /**
     * Establece seg√∫n el tipo de degradado seleccionado, el degrado a crear. Se
     * crear√° usando los colores de las variables frente y fondo
     *
     * @param evt - Evento producido por el rat√≥n
     */
    private void jBDegradadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDegradadoActionPerformed
        figura = ((VentanaInternaImagen) vi).getLienzo().getFigura();
        if (figura != null) {
            if (this.tipoDegradado == 0) {
                Point2D puntoInicial = new Point2D.Double(figura.getP1().getX(), 0);
                Point2D puntoFinal = new Point2D.Double(figura.getP2().getX(), 0);
                relleno = new RellenoDegradado(puntoInicial, frente, puntoFinal, fondo);

            } else if (this.tipoDegradado == 1) {
                Point2D puntoInicial = new Point2D.Double(0, figura.getP1().getY());
                Point2D puntoFinal = new Point2D.Double(0, figura.getP2().getY());
                relleno = new RellenoDegradado(puntoInicial, frente, puntoFinal, fondo);

            } else if (this.tipoDegradado == 2) {
                double xFinal = Math.abs(figura.getP2().getX() - figura.getP1().getX());
                double yFinal = Math.abs(figura.getP2().getY() - figura.getP1().getY());
                double distancia = Math.sqrt(Math.pow(xFinal, 2) + Math.pow(yFinal, 2));
                Point2D puntoInicial = new Point2D.Double(figura.getP1().getX(), figura.getP1().getY());
                Point2D puntoFinal = new Point2D.Double(figura.getP1().getX() + distancia / 2, figura.getP1().getY() + distancia / 2);
                relleno = new RellenoDegradado(puntoInicial, frente, puntoFinal, fondo);

            } else if (this.tipoDegradado == 3) {
                Point2D centro = new Point2D.Double(figura.getBounds().getCenterX(), figura.getBounds().getCenterY());
                Color[] colores = {this.frente, this.fondo};
                float radius = 10;
                float[] dist = {0.5f, 1.0f};
                relleno = new RellenoRadial(centro, radius, dist, colores);

            } else if (this.tipoDegradado == 4) {
                Point2D puntoInicial = new Point2D.Double(0, 0);
                Point2D puntoFinal = new Point2D.Double(0, 10);
                relleno = new RellenoDegradado(puntoInicial, frente, puntoFinal, fondo, true);
            } else if (this.tipoDegradado == 5) {
                Point2D puntoInicial = new Point2D.Double(0, 0);
                Point2D puntoFinal = new Point2D.Double(10, 0);
                relleno = new RellenoDegradado(puntoInicial, frente, puntoFinal, fondo, true);
            }
            figura.setAtributo(relleno);
        }
        try {
            this.setClosed(true);

        } catch (PropertyVetoException ex) {
            Logger.getLogger(Redimensionar.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBDegradadoActionPerformed

