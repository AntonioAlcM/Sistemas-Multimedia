    /**
     * Cambia el ancho de la curva, de las esquinas
     *
     * @param evt - Evento producido por el rat칩n
     */
    private void jAnchoCurvaStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jAnchoCurvaStateChanged
        this.rect = (RectanguloRedondeado) ((VentanaInternaImagen) vi).getLienzo().getFigura();
        if (this.rect != null && this.rect instanceof RectanguloRedondeado) {
            this.rect.setArcWidth(Integer.parseInt(this.jAnchoCurva.getValue().toString()));
            ((VentanaInternaImagen) vi).getLienzo().repaint();
        }
    }//GEN-LAST:event_jAnchoCurvaStateChanged
